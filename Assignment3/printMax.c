#include "array.h"

/*******************************************************************************
* Definitions
******************************************************************************/
#define MAX_SIZE 100

/*******************************************************************************
* Variables
******************************************************************************/
uint8_t arr[MAX_SIZE];
int arr_size = 0;

/*******************************************************************************
* @brief function to print the maximum number in the array.
******************************************************************************/
void printMax()
{
    if (arr_size == 0)
    {
        printf("Empty Array\n");
    }

    uint8_t max = arr[0];
    for (int i = 0; i < arr_size; ++i)
    {
        if (arr[i] > max) {
            max = arr[i];
        }
    }
    printf("Max number: [%d]\n", max);
}
