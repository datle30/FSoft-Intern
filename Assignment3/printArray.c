#include "array.h"

/*******************************************************************************
* Definitions
******************************************************************************/
#define MAX_SIZE 100

/*******************************************************************************
* Variables
******************************************************************************/
uint8_t arr[MAX_SIZE];
int arr_size = 0;

/*******************************************************************************
* @brief function used to print array
******************************************************************************/
void printArray()
{
	if (arr_size == 0)
	{
		printf("Empty array\n");
	}
	else
	{
		printf("Array: ");
		for (int i = 0; i < arr_size; i++)
		{
			printf("[%d] ", arr[i]);
		}
		printf("\n");
	}
}